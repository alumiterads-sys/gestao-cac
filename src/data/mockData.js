// --- HELPER: Gerar alertas de vencimento ---
export const getAlertEvents = () => {
    const events = [];
    const now = new Date();
    const in30Days = new Date(now.getTime() + 30 * 24 * 60 * 60 * 1000);
    const in7Days = new Date(now.getTime() + 7 * 24 * 60 * 60 * 1000);

    // Vencimentos de CRAF
    crafs.forEach((craf) => {
        const venc = new Date(craf.vencimentoCraf);
        if (venc <= in30Days) {
            events.push({
                id: `alert-craf-${craf.id}`,
                title: `CRAF: ${craf.cliente} - ${craf.modeloArma}`,
                date: craf.vencimentoCraf,
                type: "craf",
                urgent: venc <= in7Days,
                vencido: venc < now,
            });
        }
    });

    // Vencimentos de Guias
    guias.forEach((guia) => {
        const venc = new Date(guia.dataVencimento);
        if (venc <= in30Days) {
            events.push({
                id: `alert-guia-${guia.id}`,
                title: `Guia: ${guia.nome} - ${guia.tipoGuia}`,
                date: guia.dataVencimento,
                type: "guia",
                urgent: venc <= in7Days,
                vencido: venc < now,
            });
        }
    });

    // Vencimentos de CR
    clients.forEach((client) => {
        const venc = new Date(client.cr_expiry);
        if (venc <= in30Days) {
            events.push({
                id: `alert-cr-${client.id}`,
                title: `CR: ${client.nome}`,
                date: client.cr_expiry,
                type: "cr",
                urgent: venc <= in7Days,
                vencido: venc < now,
            });
        }
    });

    // Vencimentos SIMAF
    simaf.forEach((s) => {
        const venc = new Date(s.dataVencimento);
        if (venc <= in30Days) {
            events.push({
                id: `alert-simaf-${s.id}`,
                title: `SIMAF: ${s.cliente} - ${s.fazenda}`,
                date: s.dataVencimento,
                type: "simaf",
                urgent: venc <= in7Days,
                vencido: venc < now,
            });
        }
    });

    return events.sort((a, b) => new Date(a.date) - new Date(b.date));
};
